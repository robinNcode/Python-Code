#Use of range() function

num=list(range(10))#here starting number 0 it will generete till 9.
print()
print("List of num =",num)


num2=list(range(5,10))#here starting number 5 it will generete till 9.
print()
print("List of num2 =",num2)


num3=list(range(2,21,2))#here starting number 2 it will generete till 20 & last 2 is a increment part of range.
print()
print("List of num3 =",num3)